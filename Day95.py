#Menentukan angka positif dan negatif
angka = int(input("Tulis Sebuah Angka: "))
print()
if angka > 0:
   print("Positif ", angka, "atau biasa di tulis", angka)

#Menampilkan Kondisi Angka Nol   
elif angka == 0:
   print("Angka", angka, "atau biasa di tulis", angka)

#Menampilkan Kondisi Angka Negatif
else:
   print("Negatif", angka, "atau biasa di tulis", angka)